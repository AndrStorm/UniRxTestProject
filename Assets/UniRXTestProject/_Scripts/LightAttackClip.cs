public class LightAttackClip : StateMachineBehaviour
{
    

    public static IObservable<int> OnAttackFinished => _onAttackFinished;
    private static readonly Subject<int> _onAttackFinished = new();
    
    public static IObservable<int> OnAttackStarted => _onAttackStarted;
    private static readonly Subject<int> _onAttackStarted = new();

    public int attackEndDelay = 100;

    public override void OnStateExit(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        base.OnStateExit(animator, stateInfo, layerIndex);
        _onAttackFinished.OnNext(attackEndDelay);
    }

    public override void OnStateEnter(Animator animator, AnimatorStateInfo stateInfo, int layerIndex)
    {
        base.OnStateEnter(animator, stateInfo, layerIndex);
        _onAttackStarted.OnNext(attackEndDelay);
    }

